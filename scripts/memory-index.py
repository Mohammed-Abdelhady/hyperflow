#!/usr/bin/env python3
"""Derive `.hyperflow/memory/index.md` and `.checksums` from the memory files.

The index is derived state, never hand-authored: every entry heading in the
memory category files is parsed for a date, a title, and tags, tiered by age
(hot <=7d, warm <=30d, cold beyond), and written back as the index table. The
`.checksums` sidecar is refreshed in the same pass so the session-start
compaction advisory has line counts to compare against.

Prints the hot-tier render (hot entries + anti-patterns) to stdout for the
session-start hook to inject. Silent no-op when the project has no memory dir.

Stdlib only. Never fails the session: unreadable files are skipped.
"""

from __future__ import annotations

import hashlib
import json
import re
import sys
from datetime import date, datetime, timezone
from pathlib import Path

HOT_DAYS = 7
WARM_DAYS = 30

# Written by the session-start hook and the bridge, not by the memory protocol.
EXCLUDED = {"index.md", "session-context.md", "doctrine.md"}

# Permanently hot regardless of entry age — every worker loads it (memory-system.md).
ALWAYS_HOT = "anti-patterns.md"

MAX_RENDER_LINES = 200
MAX_ENTRY_LINES = 25

GENERATED_MARKER = "<!-- generated by scripts/memory-index.py — derived from the memory files, do not hand-edit -->"

DATE_RE = re.compile(r"(\d{4}-\d{2}-\d{2})")
HEADING_RE = re.compile(r"^(#{2,3})\s+(.*)$")
# `[domain, type]` or [domain, type] — never a bare date, which DATE_RE owns.
TAGS_RE = re.compile(r"`?\[([a-z0-9,\s-]+)\]`?")
# The whole date-bearing group, so `(2026-05-15, chain: foo)` leaves no crumbs.
DATE_GROUP_RE = re.compile(r"[\[(][^\])]*\d{4}-\d{2}-\d{2}[^\])]*[\])]")
STUB_SUFFIX_RE = re.compile(r"—\s*summarized.*$")


class Entry:
    def __init__(self, filename: str, title: str, day: date | None, tags: list[str]) -> None:
        self.filename = filename
        self.title = title
        self.day = day
        self.tags = tags
        self.body: list[str] = []

    @property
    def tier(self) -> str:
        if self.filename == ALWAYS_HOT:
            return "hot"
        if self.day is None:
            return "warm"
        age = (date.today() - self.day).days
        if age <= HOT_DAYS:
            return "hot"
        if age <= WARM_DAYS:
            return "warm"
        return "cold"

    @property
    def sort_key(self) -> tuple[int, str]:
        return (1, self.day.isoformat()) if self.day else (0, "")


def to_date(raw: str) -> date | None:
    """`YYYY-MM-DD` → date. None when the regex matched but the calendar didn't (`2026-13-45`)."""
    try:
        return datetime.strptime(raw, "%Y-%m-%d").date()
    except ValueError:
        return None


def parse_heading(text: str) -> tuple[str, date | None, list[str]]:
    """Split an entry heading into title, date, and tags.

    Accepts both the documented form — `### [2026-05-15] Title `[api, convention]`` —
    and the form writers actually emit — `## Title (2026-05-15, source-slug)`.
    """
    text = STUB_SUFFIX_RE.sub("", text)

    tags: list[str] = []
    for match in TAGS_RE.finditer(text):
        inner = match.group(1)
        if DATE_RE.search(inner):
            continue
        tags = [t.strip() for t in inner.split(",") if t.strip()]
        text = text.replace(match.group(0), " ", 1)
        break

    day = None
    group = DATE_GROUP_RE.search(text)
    raw_date = DATE_RE.search(group.group(0) if group else text)
    if raw_date:
        day = to_date(raw_date.group(1))
        # Drop the whole `(2026-05-15, chain: foo)` group, not just the date inside it.
        text = text.replace(group.group(0) if group else raw_date.group(1), " ", 1)

    return re.sub(r"\s{2,}", " ", text).strip(" ,-—`"), day, tags


def parse_file(path: Path) -> list[Entry]:
    try:
        text = path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return []

    entries: list[Entry] = []
    for line in text.splitlines():
        heading = HEADING_RE.match(line)
        if heading:
            title, day, tags = parse_heading(heading.group(2))
            if title:
                entries.append(Entry(path.name, title, day, tags))
            continue
        if entries:
            entries[-1].body.append(line)

    # anti-patterns.md carries its recency in the body (`last seen: YYYY-MM-DD`),
    # not the heading — hoist the latest one so the index shows a real date.
    for entry in entries:
        if entry.day is None:
            days = [m.group(1) for m in map(DATE_RE.search, entry.body) if m]
            if days:
                entry.day = to_date(max(days))
    return entries


def render_index(entries: list[Entry]) -> str:
    stamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%MZ")
    out = [
        "# Memory Index",
        "",
        GENERATED_MARKER,
        f"<!-- refreshed {stamp} -->",
        "",
    ]
    if not entries:
        out += ["_No memory entries yet._", ""]
        return "\n".join(out)

    counts = {"hot": 0, "warm": 0, "cold": 0}
    for entry in entries:
        counts[entry.tier] += 1
    out += [
        f"{len(entries)} entries — {counts['hot']} hot · {counts['warm']} warm · {counts['cold']} cold",
        "",
        "| Date | Tier | File | Title | Tags |",
        "|------|------|------|-------|------|",
    ]
    for entry in sorted(entries, key=lambda e: e.sort_key, reverse=True):
        day = entry.day.isoformat() if entry.day else "—"
        title = entry.title.replace("|", "\\|")
        out.append(
            f"| {day} | {entry.tier} | {entry.filename} | {title} | {', '.join(entry.tags) or '—'} |"
        )
    out.append("")
    return "\n".join(out)


def render_hot(entries: list[Entry]) -> str:
    """The block the session-start hook injects — hot entries in full."""
    hot = [e for e in entries if e.tier == "hot" and e.filename != ALWAYS_HOT]
    anti = [e for e in entries if e.filename == ALWAYS_HOT]
    if not hot and not anti:
        return ""

    out: list[str] = []
    budget = MAX_RENDER_LINES

    def emit(group: list[Entry], heading: str) -> None:
        nonlocal budget
        if not group:
            return
        out.append(heading)
        dropped = 0
        for entry in sorted(group, key=lambda e: e.sort_key, reverse=True):
            body = [b for b in entry.body if b.strip()][:MAX_ENTRY_LINES]
            cost = len(body) + 2  # heading + body + trailing blank
            if cost > budget:
                dropped += 1
                continue
            day = f" ({entry.day.isoformat()})" if entry.day else ""
            out.append(f"### {entry.title}{day} — {entry.filename}")
            out.extend(body)
            out.append("")
            budget -= cost
        if dropped:
            out.append(f"<!-- {dropped} more entries in the index — read the file to load them -->")
            out.append("")

    emit(hot, "## Project memory — hot entries (last 7 days)")
    emit(anti, "## Known anti-patterns (always enforced)")
    return "\n".join(out).rstrip() + "\n"


def write_if_changed(path: Path, content: str) -> None:
    # Skipping the no-op write keeps mtime stable, so a same-minute rerun touches nothing.
    try:
        if path.read_text(encoding="utf-8") == content:
            return
    except (OSError, ValueError):
        pass  # absent, unreadable, or not valid UTF-8 (ValueError) — fall through and rewrite it
    try:
        path.write_text(content, encoding="utf-8")
    except OSError:
        pass


def write_checksums(memory_dir: Path, files: list[Path]) -> None:
    checksums: dict[str, dict[str, object]] = {}
    for path in files:
        try:
            raw = path.read_bytes()
        except OSError:
            continue
        checksums[f".hyperflow/memory/{path.name}"] = {
            "sha256": hashlib.sha256(raw).hexdigest(),
            "lineCount": raw.count(b"\n") + (1 if raw and not raw.endswith(b"\n") else 0),
        }
    write_if_changed(memory_dir / ".checksums", json.dumps(checksums, indent=2) + "\n")


def main(argv: list[str]) -> int:
    if len(argv) < 2:
        print("usage: memory-index.py <.hyperflow dir>", file=sys.stderr)
        return 2

    memory_dir = Path(argv[1]) / "memory"
    if not memory_dir.is_dir():
        return 0

    files = sorted(p for p in memory_dir.glob("*.md") if p.name not in EXCLUDED)
    entries: list[Entry] = []
    for path in files:
        entries += parse_file(path)

    write_if_changed(memory_dir / "index.md", render_index(entries))
    write_checksums(memory_dir, files)

    hot = render_hot(entries)
    if hot:
        sys.stdout.write(hot)
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv))
